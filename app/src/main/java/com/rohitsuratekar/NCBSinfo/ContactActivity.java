    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item) {
        // Handle navigation view item clicks here.
        int id = item.getItemId();

        if (id == R.id.nav_home) {
            Intent intent = new Intent(ContactActivity.this, MainActivity.class);
            startActivity(intent);

        } else if (id == R.id.nav_full_shuttle) {

         Intent intent = new Intent(ContactActivity.this, AllShuttles.class);
         intent.putExtra("switch", String.valueOf(0));
         startActivity(intent);

        } else if (id == R.id.nav_settings) {
            Intent intent;
            intent = new Intent(ContactActivity.this, SettingsActivity.class);
            intent.putExtra(SettingsActivity.EXTRA_SHOW_FRAGMENT, SettingsActivity.DefaultShuttleFragment.class.getName());
            intent.putExtra(SettingsActivity.EXTRA_NO_HEADERS, true);
            startActivity(intent);

        } else if (id == R.id.nav_full_buggy) {
            Intent intent = new Intent(ContactActivity.this, NCBStoMandara.class);
            intent.putExtra("switch", String.valueOf(4));
            startActivity(intent);

        } else if (id == R.id.nav_other) {
            Intent intent = new Intent(ContactActivity.this, NCBStoOther.class);
            intent.putExtra("switch", "0");
            startActivity(intent);
            return true;
        }

            DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
            drawer.closeDrawer(GravityCompat.START);
            return true;

    }

